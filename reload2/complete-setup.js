        await executeSQL(createMetricsTable);
        console.log('âœ… executive_metrics table created');

        // Create users table
        console.log('ðŸ‘¥ Creating users table...');
        const createUsersTable = `
        CREATE TABLE IF NOT EXISTS users (
            id UUID PRIMARY KEY,
            email TEXT NOT NULL UNIQUE,
            role TEXT NOT NULL DEFAULT 'viewer' CHECK (role IN ('admin', 'editor', 'viewer')),
            created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW() NOT NULL,
            updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW() NOT NULL,
            last_login TIMESTAMP WITH TIME ZONE
        );
        `;
        await executeSQL(createUsersTable);
        console.log('âœ… users table created');

        // Create audit_logs table
        console.log('ðŸ“ Creating audit_logs table...');
        const createAuditTable = `
        CREATE TABLE IF NOT EXISTS audit_logs (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            user_id UUID NOT NULL,
            action TEXT NOT NULL,
            table_name TEXT NOT NULL,
            record_id TEXT NOT NULL,
            old_data JSONB,
            new_data JSONB,
            created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW() NOT NULL
        );
        `;
        await executeSQL(createAuditTable);
        console.log('âœ… audit_logs table created');

        // Create scenarios table
        console.log('ðŸŽ¯ Creating scenarios table...');
        const createScenariosTable = `
        CREATE TABLE IF NOT EXISTS scenarios (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW() NOT NULL,
            updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW() NOT NULL,
            name TEXT NOT NULL,
            description TEXT,
            factors JSONB NOT NULL,
            results JSONB,
            created_by UUID,
            is_active BOOLEAN DEFAULT true
        );
        `;
        await executeSQL(createScenariosTable);
        console.log('âœ… scenarios table created');

        // Create triggers for updated_at
        console.log('âš¡ Creating update triggers...');
        const createTrigger = `
        CREATE OR REPLACE FUNCTION trigger_set_timestamp()
        RETURNS TRIGGER AS $$
        BEGIN
            NEW.updated_at = NOW();
            RETURN NEW;
        END;
        $$ LANGUAGE plpgsql;
        `;
        await executeSQL(createTrigger);

        const triggers = [
            'executive_metrics',
            'users', 
            'audit_logs',
            'scenarios'
        ];

        for (const table of triggers) {
            const dropTrigger = `DROP TRIGGER IF EXISTS set_${table}_timestamp ON ${table};`;
            await executeSQL(dropTrigger);
            
            const createTrigger = `
            CREATE TRIGGER set_${table}_timestamp
            BEFORE UPDATE ON ${table}
            FOR EACH ROW
            EXECUTE FUNCTION trigger_set_timestamp();
            `;
            await executeSQL(createTrigger);
        }
        console.log('âœ… Update triggers created');

        // Insert initial metrics if table is empty
